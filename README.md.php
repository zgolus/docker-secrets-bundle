Docker Secrets Bundle
=======================

#### Installation:

- Add repository to `composer.json`

```
{
    ...
    "repositories": [
        ...
        {
            "type": "vcs",
            "url": "git@github.com:zgolus/docker-secrets-bundle.git"
        }
    ]
    ...
}
```
- `composer require 'zgolus/docker-secrets-bundle:^0.1.0',`
- `composer install`
